package com.jobreporting.classes;

/**
 *
 * @author santi
 */
public class BookFormated {
    private int id;
    private String customerName;
    private String customerSurname;
    private String hotelName;
    private String roomService;
    private String gymService;
    private int numberNights;
    private String start;
    private String end;
    private String paid;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the customerName
     */
    public String getCustomerName() {
        return customerName;
    }

    /**
     * @param customerName the customerName to set
     */
    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    /**
     * @return the customerSurname
     */
    public String getCustomerSurname() {
        return customerSurname;
    }

    /**
     * @param customerSurname the customerSurname to set
     */
    public void setCustomerSurname(String customerSurname) {
        this.customerSurname = customerSurname;
    }

    /**
     * @return the hotelName
     */
    public String getHotelName() {
        return hotelName;
    }

    /**
     * @param hotelName the hotelName to set
     */
    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }

    /**
     * @return the roomService
     */
    public String getRoomService() {
        return roomService;
    }

    /**
     * @param roomService the roomService to set
     */
    public void setRoomService(String roomService) {
        this.roomService = roomService;
    }

    /**
     * @return the gymService
     */
    public String getGymService() {
        return gymService;
    }

    /**
     * @param gymService the gymService to set
     */
    public void setGymService(String gymService) {
        this.gymService = gymService;
    }

    /**
     * @return the numberNights
     */
    public int getNumberNights() {
        return numberNights;
    }

    /**
     * @param numberNights the numberNights to set
     */
    public void setNumberNights(int numberNights) {
        this.numberNights = numberNights;
    }

    /**
     * @return the start
     */
    public String getStart() {
        return start;
    }

    /**
     * @param start the start to set
     */
    public void setStart(String start) {
        this.start = start;
    }

    /**
     * @return the end
     */
    public String getEnd() {
        return end;
    }

    /**
     * @param end the end to set
     */
    public void setEnd(String end) {
        this.end = end;
    }

    /**
     * @return the paid
     */
    public String getPaid() {
        return paid;
    }

    /**
     * @param paid the paid to set
     */
    public void setPaid(String paid) {
        this.paid = paid;
    }
    
}
